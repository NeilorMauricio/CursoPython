# Faça um programa que leia um número e mostre seu sucessor e antecessor.
n = int(input('Digite um número:'))
print(f'\n'
      f'O número digitado foi: {n} \n'
      f'O número inteiro é: {int(n)} \n'
      f'O número real é: {float(n)} \n'
      f'O número inteiro mais 1 é: {int(n) + 1} \n'
      f'O número inteiro menos 1 é: {int(n) - 1} \n'
      f'O número inteiro multiplicado por 2 é: {int(n) * 2} \n'
      f'O número inteiro dividido por 2 é: {int(n) / 2} \n')
